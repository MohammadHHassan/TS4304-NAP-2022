package day3_pm;

public class LearningMethod
{
	public static void main(String[] args)
	{
		int sum1=0;
		
		for(int i=1 ; i<=5 ; i++)
		{
			sum1 = sum1+i;
		}
		
		System.out.println("The first sum = " + sum1);
		
		int sum2=0;
		
		for(int j=10 ; j<=15 ; j++)
		{
			sum2 = sum2+j;
		}
		
		System.out.println("The second sum = " + sum2);
		
		int sum3=0;
		
		for(int i=20 ; i<=25 ; i++)
		{
			sum3 = sum3+i;
		}
		
		System.out.println("The third sum = " + sum3);
	}
}
