package day2_pm;

public class LearningDoWhile
{
	public static void main(String[] args)
	{
		int i=11;		// Declare variable int i as 11
		
		do
		{
			System.out.println(i + ". Welcome to Java Programming!");
			i++;		// Increment i by 1
		}while(i<=10);	// Do while condition is i less than or equal to 10
		
		System.out.println("Thank you");
	}
}
