package day2_pm;

public class LearningWhile
{
	public static void main(String[] args)
	{
		int i=11;		// Declare variable int i as 11
		
		while(i<=10)	// Loop control while i is less than or equal to 10
		{
			System.out.println(i + ". Welcome to Java Programming!");
			i++;		// Increment i by 1
		}
		
		System.out.println("Thank you");
	}
}
